async function seed() {
    const treksCol = collection(db, 'treks');

    try {
        console.log('Cleaning up existing treks...');
        const snapshot = await getDocs(treksCol);
        for (const doc of snapshot.docs) {
            await deleteDoc(doc.ref);
        }

        console.log(`AI Agents are populating ${sampleTreks.length} trails across Europe...`);
        for (const trek of sampleTreks) {
            console.log(`Processing Trail: ${trek.name} (${trek.city}, ${trek.country})`);
            await addDoc(treksCol, {
                ...trek,
                gpxUrl: trek.gpxUrl || "https://example.com/trail.gpx" // Fallback
            });
        }

        console.log('European Trek Database fully updated with AI-fetched records!');
    } catch (error) {
        console.error('Data pipeline error:', error);
    }
}

seed();
